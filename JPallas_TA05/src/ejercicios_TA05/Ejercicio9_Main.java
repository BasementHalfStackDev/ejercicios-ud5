package ejercicios_TA05;

public class Ejercicio9_Main {

	public static void main(String[] args) {

		for (int i = 1; i <= 100; i++) {
			if (i % 2 == 0) {
				System.out.println(i + " Es divisible entre 2.");
			} else if (i % 3 == 0) {
				System.out.println(i + " Es divisible entre 3.");
			}
		}

	}

}
